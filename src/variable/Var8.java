public class Var8 {
    static void main(String[] args) {
        //정수
        byte b = 127; //-128 ~ 127
        short s = 32767; //-32768 ~ 32767
        int i = 2147483647; //-2147483648 ~ 2147483647

        //-9223372036854775808 ~ 9223372036854775807
        long l = 9223372036854775807L;

        //실수
        float f = 10.0f;
        double d = 10.0;
    }
}
